	<h3>DIARREA NEONATAL</h3>

	<p>La diarrea neonatal es una enfermedad multifactorial producida por un conjunto 
	de causas complejas, entre las que podemos nombrar:  factores inmunológicos, 
	nutricionales, manejo del rodeo, factores de estrés y la presencia de agentes 
	infecciosos.</p>
	
	<p>Los terneros recién nacidos son muy susceptibles a las diarreas, que comienzan a 
	presentarse desde las 12 hs hasta aproximadamente los 30 días de vida. 
	Conociendo esto, es fundamental que para proponer un plan sanitario de 
	prevención exitoso, debemos tener en cuenta todos los factores de riesgo.</p>
	
	<h4> Factores inmunológicos	</h4>
	<p>Debido al tipo de placenta del bovino en que los anticuerpos de la madre no 
	pueden pasar al feto “in útero”,  juega un rol fundamental el manejo del calostro 
	que le da al ternero la protección necesaria contra los agentes infecciosos durante 
	las primeras semanas de vida (inmunidad pasiva) y le permite sobrevivir hasta que 
	desarrolle sus propias defensas (inmunidad activa).</p>
	
	<p>En el manejo del calostro es primordial tener en cuenta tres puntos:
	1 Calidad del calostro: el calostro importante es el del 1° ordeñe o primera 
	mamada. Debe contener 50 gr de inmunoglobulina G por litro. En los tambos es 
	posible controlar la calidad del calostro y suministrar los de mejor aptitud a los 
	recién nacidos. </p>
	
	<p>Tanto en la cría como en la producción lechera se debe tener en cuenta la edad 
	de la vaca (el de las vaquillonas es de menor calidad) y el estado corporal para 
	que el calostro producido sea de alta calidad. </p>
	
	<p>2 Cantidad de calostro ingerido: Debe consumir el 10-12% del peso corporal de 
	calostro de buena calidad para disminuir la predisposición a contraer 
	enfermedades infecciosas.</p>
	
	<p>3 Tiempo de consumo y absorción: es importante consumir la mitad de la 
	cantidad antes de las 6 hs de vida y el resto antes de las 12 hs de vida del ternero 
	para asegurarse una buena protección. </p>
	
	<h4> Factores nutricionales </h4>
	<p>El estado corporal de la madre es fundamental para que produzca la cantidad y 
	calidad tanto de calostro como de leche que asegure una menor incidencia de 
	diarreas en el ternero. </p>
	
	<h4> Manejo del rodeo </h4>
	<p>Un punto fundamental es el lugar del parto y el lugar de permanencia durante las 
	primeras semanas de vida. Debe ser un lugar con niveles mínimos de 
	contaminación y teniendo en cuenta el confort ambiental para disminuir los riesgos 
	de infección y desarrollo de la enfermedad.<br />
	Otro punto a tener en cuenta es el hacinamiento que contribuye al contagio. </p>
	
	<p>Deben rotarse los potreros de parto y permanencia durante el primer mes de vida 
	para disminuir la oferta de agentes infecciosos al ternero.</p>
	
	<h4>Agentes infecciosos</h4>
	<p>Las causas infecciosas más comunes en la diarrea neonatal son: 
	<strong>Virus:</strong> principalmente Rotavirus y en menor medida Coronavirus. 
	<strong>Bacterias:</strong> Escherichia coli es uno de los agentes patógenos más importantes en 
	los primeros 15 días de vida. Salmonella tiene relevancia principalmente en 
	terneros de más de 30 días de vida.</p>
	
	
	<p><strong>Parásitos:</strong> criptosporidios y coccidios se presentan generalmente en terneros de 
	más de 15 días y en combinación con otros patógenos virales y bacterianos.
	Signos clínicos de la Diarrea neonatal:<br />
	El principal signo es la DIARREA que esta determinada por el aumento en la 
	cantidad de materia fecal y el porcentaje de materia seca de la misma. 
	Normalmente la consistencia (aguachenta) y forma (sin forma) de la misma, son 
	las que identificamos como principal característica, muchas veces acompañada de 
	mucus o sangre.</p>
	
	
	<p>Esto lleva al ternero a distintos grados de deshidratación dependiendo del agente 
	y la precocidad en la identificación para instaurar el tratamiento. <br />
	La deshidratación en casos graves lleva a la postración, coma y muerte que se 
	puede dar en el término de 2 a 4 días.</p>
	
	<p>En el caso de terneros que no lleguen a la muerte las consecuencias son pérdida 
	de peso, debilidad y retraso en el crecimiento.</p>
	
	<p><strong>Diagnóstico:</strong>
	Se realiza a partir de la sintomatología clínica y la epidemiología. Es 
	recomendable realizar pruebas complementarias para determinar las causas y los 
	agentes infecciosos involucrados. De esta manera se pueden implementar 
	tratamientos efectivos y un correcto plan de prevención.</p>
	
	<p><strong>Tratamiento:</strong><br />
	Los puntos fundamentales son:<br />
	La hidratación: puede hacerse en forma oral, con sales rehidratantes para 
	administración oral, o en forma endovenosa dependiendo de la gravedad del caso. <br />
	
	El uso de antibióticos: es fundamental tener un diagnostico bacteriológico para la 
	elección del mismo. (link a ECCT y línea de ATB)</p>
	
	<p><strong>Prevención:</strong><br />
	Tener en cuenta los factores de riesgo descriptos e implementar un esquema de 
	vacunación para prevenir las casusas infecciosas. </p>
	
	<p><strong>Esquema de vacunación sugerido: (link a Rotatec J5)</strong> <br />
	Primo vacunación (cualquier categoría que recibe esta vacuna por primera vez): 2 
	dosis con intervalo de 21 a 35 días. <br />
	
	Rodeos de cría: vacas y vaquillonas (primo vacunación): Primera dosis: 7º mes de 
	preñez; Segunda dosis: 8º mes de preñez.<br />
	
	Vacas y Vaquillonas (revacunación): aplicar una dosis al 8º mes de preñez. <br />
	
	Se sugiere en zonas de alta incidencia dar una tercera dosis a las vacas cola de 
	parición.<br />
	
	<strong>Tambo:	</strong>vacas y vaquillonas (primo vacunación):<br />

	Primera dosis: 7º mes de preñez; <br />
	
	Segunda dosis: 8º mes de preñez. <br />
	
	Vacas y vaquillonas (revacunación): primera dosis, al secado; segunda dosis, al 
	
	pre parto. </p>
	
	
	<h4>ENFERMEDADES CLOSTRIDIALES</h4>
	
	<p>Las enfermedades clostridiales están causadas por una familia de bacterias 
	denominadas Clostridium.</p>
	
	<p>Estos microrganismos están muy difundidos en el ambiente y se encuentran 
	normalmente en el suelo y en las heces. Forman esporas, un estadio muy 
	resistente que le permite sobrevivir en el ambiente durante mucho tiempo. Para 
	que aparezca la enfermedad se tienen que dar condiciones específicas para que 
	la bacteria invada los tejidos y produzca las toxinas que son, en definitiva, las que 
	producen las lesiones y muerte de los animales afectados. Estas condiciones son 
	heridas, intervenciones quirúrgicas sin asepsia, partos con lesiones, etc. También 
	se da ante cambios repentinos de dieta, sobrealimentación, o infecciones con 
	parásitos.</p>
	
	<p>Las enfermedades que provocan son siempre agudas y mortales, casi nunca 
	permiten instaurar un tratamiento efectivo.</p>
	
	<p>Entre las más conocidas podemos destacar:</p>
	
	o Mancha (C. chauvoei) <br />
	
	o Gangrena gaseosa (C. septicum) <br />
	
	o Hemoglobinuria bacilar (C. haemolyticum) <br />
	
	o Botulismo (C. botulinum)<br />
	
	o Enterotoxemia (C. perfringens tipos A, B, C y D).<br />
	
	o C. sordellii.<br />
	
	o Hepatitis necrótica (C. novyi).<br />
	
	o Tétanos (C. tetani).<br />
	
	A continuación describiremos particularidades de las principales afecciones por 
	
	clostridios.<br />
	
	<strong>MANCHA</strong>
	
	<p>La mancha, también conocida como pierna negra, cuarto negro, carbunclo 
	sintomático y gangrena enfisematosa producida por C. chauvoei. En bovinos 
	afecta especialmente a animales de 3 meses a 2 años de vida. </p>
	
	<p>Clínicamente se observa en los animales afectados renguera o manquera casi 
	como único signo. Principalmente se afectan músculos de la zona de cuello, 
	espalda, intercostal y cuartos traseros. Las lesiones tienen aspecto como carne 
	cocida y gas en las masas musculares. </p>
	
	<p>Para su diagnóstico es importante la toma de muestras y aislamiento del agente. </p>
	
	
	<h4>EDEMA MALIGNO (GANGRENA GASEOSA)</h4>
	
	<p>También llamado edema gaseoso o gangrena gaseosa es una enfermedad 
	generalmente mortal, producida por C. septicum.</p>
	
	<p>Esta enfermedad se desarrolla cuando hay heridas en la piel, tracto digestivo o 
	zona genital, intervenciones quirúrgicas sin asepsia, utilización de jeringas y/o 
	agujas sucias, etc.</p>
	
	<p>En el lugar afectado se observan áreas edematosas como principal signo clínico.</p>
	
	<p>Como medidas de prevención es fundamental la higiene en intervenciones 
	quirúrgicas como castración o descole y utilización de material estéril para la 
	aplicación de medicamentos o vacunas. </p>
	
	<h4>HEMOGLOBINURIA BACILAR</h4>
	
	<p>Es una enfermedad aguda producida por C. haemolyticum (Clostridium novyi tipo 
	D). El bovino es el principal afectado aunque también ocurre en ovinos. Sus 
	características clínicas son hemoglobinuria (orina con sangre) e ictericia (mucosas 
	amarillentas) y la presencia de infartos en hígado.</p>
	
	<p>Es una enfermedad que se presenta en zonas determinadas, como pre cordillera 
	patagónica, cuenca del salado y campos bajos de mesopotamia. Dentro de los 
	factores predisponentes podemos mencionar lesiones en el hígado  producidas 
	por las larvas migrantes de parásito Fasciola hepatica, alteraciones metabólicas, 
	preñez avanzada, plantasy químicos hepatotóxicos, etc.</p>
	
	<p>Normalmente cursa sin signos clínicos encontrándose los animales muertos. La 
	mortalidad es de aproximadamente un 95 %.</p>
	
	<p>El infarto del hígado es prácticamente patognomónico; suele estar acompañado de 
	orina roja como vino tinto en vejiga. Estos dos hallazgos son importantes para el 
	diagnóstico a campo.</p>
	
	<p>Para su prevención es recomendable controlar la Fasciola hepatica con 
	desparasitaciones estratégicas.(link a antiparasitarios faciolicidas)</p>
	
	<h4>BOTULISMO</h4>
	
	<p>También conocida como “Mal de Aguapey”.</p>
	
	<p>La enfermedad se da por la ingestión de una toxina producida por C. botulinum. La 
	toxina se ingiere con el alimento y es resistente a los procesos digestivos. Los 
	bovinos, ovinos, equinos y aves son especies susceptibles.</p>
	
	<p>En los bovinos la intoxicación se da por la ingestión de huesos o alimentos 
	contaminados por la toxina. La ingestión de huesos (osteofagia) se da por 
	deficiencia de fosforo en el suelo y es la principal causa de ingestión de la toxina.</p>
	
	<p>Los signos clínicos aparecen a los pocos días observándose parálisis muscular 
	progresiva que comienza siempre por el tren posterior y termina con la muerte por 
	parálisis respiratoria.</p>
	
	<p>Para el diagnóstico es importante la observación de pica y osteofagia. A la 
	necropsia no hay lesiones características de Botulismo. Encontrar cuerpos 
	extraños (huesos, piedras, etc.) en rumen, en una zona endémica y la falta de otro 
	tipo de lesiones hace sospechar de esta enfermedad. </p>
	
	<p>Para la prevención es importante la quema de animales muertos y corregir la 
	deficiencia de fosforo en la dieta para evitar pica y osteofagia.</p>
	
	<p>Las vacunas para enfermedades clostridiales constituyen herramientas probadas 
	para prevenir mortandades por estos agentes. (link a Triplepoligen, Bioclostrigen y 
	Policlostrigen)</p>
	
	
	<h4>COMPLEJO RESPIRATORIO BOVINO O ENFERMEDAD  RESPIRATORIA BOVINA</h4>
	
	<p>El COMPLEJO RESPIRATORIO BOVINO (CRB) O ENFERMEDAD 
	RESPIRATORIA BOVINA (ERB) también llamada FIEBRE DEL TRANSPORTE o 
	NEUMONIA es una enfermedad que afecta a animales jóvenes (de 4 meses a 2 
	años de edad), aunque se puede dar también en adultos. </p>
	
	<p>Es producida principalmente por las bacterias Pasteurella, Manheimia  y/o 
	Histophilus. Las infecciones por virus de la Rinotraqueítis Infecciosa Bovina (IBR), 
	Diarrea Viral Bovina (DVB), Parainfluenza 3 (PI3) y virus Sincitial Respiratorio 
	(VRSB) y situaciones de estrés (destete, transporte, cambio de alimentación, 
	amplitud térmica, etc.) son determinantes para la aparición de la enfermedad.</p>
	
	<p>Otros agentes que suelen predisponer a la aparición de la enfermedad son hongos  
	o parásitos pulmonares.</p>
	
	<p>Las neumonías tienen un periodo de entre 5 y 14 de días de incubación 
	posteriores al estrés o factores predisponentes. </p>
	
	
	<strong>Signos clínicos:</strong>
	
	<p>Se pueden presentar animales con respiración superficial y frecuencia respiratoria 
	
	aumentada; tos, que se observa con facilidad al revisar los animales a la mañana 
	
	temprano y si se los hace caminar, anorexia, secreción nasal serosa al inicio y 
	
	muco‐purulenta con el avance de la enfermedad, nariz costrosa, lagrimeo y fiebre 
	
	que puede llegar en algunos casos a 41 ºC.</p>
	
	
	<strong>Diagnóstico:</strong>
	
	<p>Es importante la anamnesis y la identificación de las lesiones, con toma de 
	
	muestras de pulmón en condiciones asépticas para aislamiento bacteriológico. La 
	
	historia de esta información nos permitirá tomar una correcta decisión a la hora de 
	
	elegir el antibiótico para un tratamiento efectivo.</p>
	
	
	<strong>Tratamiento:</strong>
	
	<p>Es importante la identificación precoz de los animales afectados para instaurar un 
	
	tratamiento efectivo. En casos graves o detectados en forma tardía, los animales 
	
	pueden convertirse en enfermos crónicos o fracasar el tratamiento y morir. </p>
	
	<p>Se deben utilizar antibacterianos con sensibilidad demostrada para los agentes 
	
	presentes (link a todos los ATB); también se puede utilizar antiinflamatorios no 
	
	esteroides; y antiparasitarios en casos de infestación por Dictiocaulus (link línea 
	
	ATP).</p>
	
	
	<strong>Prevención y Control:</strong>
	
	<p>Se debe evitar someter a los animales a condiciones de estrés descriptas.</p>
	
	<p>La prevención con vacunas (link a las Biopoli) específicas es el método más 
	
	económico para disminuir las pérdidas ocasionadas por disminución de la 
	
	ganancia de peso, mayor edad al primer servicio, disminución de la conversión de 
	
	alimento, menor producción de leche en su primer lactancia, aumento de los 
	
	gastos en tratamiento,  mayor tiempo del personal dedicado a la atención  de los 
	
	animales enfermos y muertes. </p>
	
	
	<h4>RINOTRAQUEÍTIS INFECCIOSA BOVINA (IBR)</h4>
	
	<p>Es una enfermedad provocada por el herpes virus bovino. Hay dos tipos 
	
	descriptos. Las manifestaciones clínicas que provocan son distintas.</p>
	
	<strong>1. Herpes Virus Bovino tipo I</strong>
	
	Se presenta con una forma respiratoria. <br />
	
	Es de incidencia mundial, con alta morbilidad y baja mortalidad. <br />
	
	Es de muy fácil transmisión a través de material contaminado.  <br />
	
	Dentro de las manifestaciones clínicas podemos identificar, el morro con costras 
	
	afectando vías respiratorias altas. Avanza a vías respiratorias bajas, afectando a  
	
	tráquea y bronquios. <br />
	
	<p>Se describe como agente etiológico del CRB acompañado por otros virus o 
	
	predisponiendo para la invasión de bacterias, ocasionando infecciones bacterianas 
	
	secundarias. Estas últimas, generalmente, son las responsables de la muerte del 
	
	animal.</p>
	
	<p>Suele presentarse conjuntamente con la Forma Ocular, caracterizada por 
	
	conjuntivitis y lagrimeo intenso, no afecta la córnea, pero sí puede ser puerta de 
	
	entrada para Moraxella bovis.</p>
	
	<p>Hay una forma reproductiva. La principal manifestación en hembras es 
	
	infertilidad, muerte embrionaria y vulvovaginitis. En el toro se puede observar 
	
	balanopostitis.</p>
	
	<p>También se pude observar aborto en el último trimestre de la gestación. El aborto 
	
	puede aparecer entre una semana y los 3 meses posteriores a una manifestación 
	
	clínica en el rodeo.</p>
	
	
	<strong>2. Herpes Virus Bovino Tipo V</strong>
	
	<p>Provoca la llamada forma encefálica. El tipo V tiene características que le 
	
	permiten invadir el SNC y replicarse en el, a diferencia del tipo I.</p>
	
	<p>También ingresa y se replica en la cavidad nasal, a través de distintos nervios 
	
	llegan al SNC.</p>
	
	<p>Posee una morbilidad baja (del 2% al 10%), pero la mortalidad es alta, llegando en 
	
	muchas ocasiones al 100%.</p>
	
	<p>Los animales jóvenes (6 meses a 2 años) son los más susceptibles. Los signos 
	
	que se pueden observar son incoordinación al caminar, marcha en círculo, 
	
	salivación, rechinar de dientes, etc. No siempre se encuentran lesiones a la 
	
	necropsia.</p>
	
	
	<strong>Prevención</strong>
	
	<p>El uso de vacunas como estrategia de prevención es lo más económico y 
	
	eficiente. (link a las vacunas con IBR)</p>
	
	<h4>DIARREA VIRAL BOVINA (DVB)</h4>
	
	<p>El virus de la DVB es un Pestivirus. Se describen diferentes cepas, como los 
	
	serotipos 1a, 1b y 2. Pueden ser citopáticas (CP) o no citopáticas (NCP). Este 
	
	nombre no tiene que ver con la patogenicidad en los animales sino como una 
	
	forma de comportamiento en cultivos celulares.</p>
	
	<p>Se puede transmitir por contacto directo y vía transplacentaria. En este último 
	
	caso, puede generar nacimiento de terneros persistentemente infectados (PI). 
	
	Normalmente el 50% muere antes de los 2 años. No obstante esto, es la principal 
	
	fuente de virus en los rodeos.</p>
	
	<p>A un rodeo puede ingresar de diferentes formas, como por ejemplo al introducir 
	
	animales infectados en cualquier estadio fisiológico, en forma iatrogénica por el 
	
	uso de semen infectado o a través de agujasen la aplicación de productos 
	
	inyectables. También el uso de vacunas vivas en vacas preñadas puede ser una 
	
	fuente de infección. </p>
	
	<p>Este virus puede ser aislado de todos los fluidos naturales.</p>
	
	<p>Podemos separar a la acción del virus en dos etapas bien definidas:</p>
	
	<p>1.- Etapa de gestación, se produce una infección fetal con diferentes 
	
	anormalidades dependiendo si es un virus CP (puede producir abortos en 
	
	cualquier momento de la gestación) o NCP. </p>
	
	<p>En el caso de ser NCP, el virus es capaz de atravesar la barrera placentaria, 
	
	invadir el feto y originar diferentes anormalidades. Dependiendo del momento de 
	
	la gestación en la que se produzca la infección podemos observar distintas 
	
	manifestaciones:<br />
	
	<p>-Si se produce hasta los 120 días de gestación puede provocar aborto o 
	
	nacimiento de terneros PI.</p>
	
	<p>-Si esta se produce entre los 120 y 180 días de gestación puede ocasionar aborto 
	
	o malformaciones congénitas.</p>
	
	<p>-Si la infección sucede después de los 180 días puede provocar de igual forma 
	
	aborto aunque el feto es capaz de generar sus propios anticuerpos, nace positivo  
	
	serológicamente pero no está infectado. </p></p>
	
	<p>2.- En etapa post parto. Se pueden describir dos enfermedades: <br />
	
	 Diarrea Viral Bovina: cursa con síntomas leves o directamente subclínica. Se  
	
	caracteriza por anorexia, depresión, fiebre, suele presentar diarrea y suele 
	
	tener recuperación en pocos días. Como el VDVB produce inmunosupresión 
	
	favorece la entrada de otros virus como IBR o PI‐3 o bacterias como 
	
	Pasteurella multocida, Mannheimia haemolytica, etc. Y con esto la aparición de 
	
	cuadros de ERB. <br />
	
	 Enfermedad de las Mucosas: en este caso los PI juegan un papel muy 
	
	importante. Los animales que no mueren permanecen diseminando la infección 
	
	y en el caso de ser una hembra puede generar nuevos terneros PI. Bajo 
	
	determinadas condiciones estos animales PI se sobre infectan con cepas 
	
	citopáticas, desarrollan una enfermedad mortal de curso agudo o crónico que 
	
	se denomina Enfermedad de las Mucosas.</p>
	

	<p><strong>Signos clínicos</strong></p>
	
	<p>Si la afectada es la etapa de gestación podemos observar abortos y 
	malformaciones. </p>
	
	<p>En el caso que los animales afectados sean de la etapa post parto podemos 
	observar depresión, anorexia, aumento en la temperatura corporal pudiendo llegar 
	a 40 – 41ºC, diarrea profusa y acuosa, puede contener moco y a veces sangre. </p>
	
	<p>También se puede observan erosiones y ulceraciones en boca, esófago, pilares 
	del rumen, cuajo, intestino delgado (característico a nivel de las placas de Peyer) e 
	intestino grueso, meteorismo crónico. Al mismo tiempo se pueden encontrar 
	erosiones superficiales cubiertas por costras en periné, escroto, prepucio, vulva, 
	etc. </p>
	
	<p>Desde el punto de vista productivo hay menor GPD, menor producción de leche, 
	mayor susceptibilidad a las enfermedades y muerte.</p>
	
	<p><strong>Diagnóstico y Control</strong></p>
	
	<p>Es muy importante identificar los animales PI, que son el reservorio y 
	diseminadores de la enfermedad. La identificación de los PI se puede realizar con 
	muestreo de los animales y una correcta interpretación de los resultados y 
	estrategia para la eliminación. </p>
	
	<p>El objetivo del control es la eliminación de los PI.</p>
	
	<p>También el uso de semen y la incorporación de animales libres del virus.</p>
	
	<p><strong>Prevención</strong></p>
	
	<p>Como estrategia de prevención de la enfermedad es la utilización de vacunas. Son 
	
	a virus inactivado y debe contener los dos subtipos. (link a las vacunas con BVD)</p>
	
	
	
	<h4>QUERATOCONJUNTIVITIS INFECCIOSA BOVINA (QIB)</h4>
	
	<p>La queratoconjuntivitis infecciosa bovina afecta el globo ocular, particularmente la 
	córnea (parte transparente del ojo). Puede afectar cualquier categoría pero 
	principalmente se da en animales jóvenes.<p>
	
	<p>Si no se toman las medidas correctivas y de control se puede afectar hasta el 80% 
	de la población en el término de 3 a 4 semanas.</p>
	
	<p>Las lesiones que producen son de leves opacidades de la córnea a la pérdida del ojo.</p>
	
	<p>Si bien no es una enfermedad mortal ocasiona grandes pérdidas económicas 
	determinándose al destete hasta un 10% de diferencia con animales sanos.</p>
	
	<p><strong>Etiología:</strong></p>
	
	<p>El principal agente aislado en esta enfermedad es una bacteria, Moraxella bovis. </p>
	
	<p>Existen al menos 7 cepas de M. bovis con diferentes grados de virulencia entre 
	cepas, que es determinante en el grado de lesión que provoca. </p>
	
	<p>Otra bacteria aislada en casos de QIB es Branhamella ovis ocasionando lesiones 
	similares a Moraxella.</p>
	
	<p>En la enfermedad pueden participar virus como el de la Rinotraqueítis Infecciosa 
	Bovina (IBR) que provoca conjuntivitis, actuando como una puerta de entrada de 
	las bacterias. </p>
	
	<p>Otros agentes que se han aislado en la QIB, aunque en menor medida, son 
	Clamydia y Mycoplasma bovis.</p>
	
	<p>Hay factores predisponentes que tienen mucha importancia en la presentación de 
	la enfermedad. Entre ellos podemos nombrar la radiación solar, polvo en los 
	alimentos o en el ambiente, viento, elementos traumáticos (pasturas encañadas o 
	enmalezadas con cardos, sorgos o maíces diferidos, etc.) y las moscas como 
	vector de los microorganismos infecciosos.</p>
	
	<p>Otro factor a tener en cuenta es el estado inmunitario de los animales y 
	situaciones de estrés (destete, viajes prolongados, inclemencias ambientales, 
	cambios en bruscos en la alimentación, manejo, etc.) que puede ser 
	determinantes para la aparición de un brote de QIB.</p>
	
	<p>Razas tipo Bos taurus (razas británicas) son más susceptibles que razas Bos 
	indicus (razas cebuinas).</p>
	
	<p><strong>Manifestaciones clínicas:</strong></p>
	
	<p>Los primeros signos aparecen a los 2 a 3 días, hasta las 3 semanas desde la 
	aparición de los primeros animales con síntomas. </p>
	
	<p>Los primeros signos que se observan a campo son lagrimeo, fotofobia y anorexia 
	principalmente por dolor. Cuando se observa más en detalle el ojo afectado se 
	observa hiperemia de los vasos corneales y la inflamación de la conjuntiva. </p>
	
	<p>Con el avance de la enfermedad, en 1 o 2 días se puede observar opacidad en la 
	córnea que comienza desde el centro hacia afuera, llega a cubrir toda la córnea. </p>

	<p>En casos graves y que no se tratan a tiempo, la córnea puede ulcerarse y forma 
	como un cono (queratocono), se puede observar pus amarillo y llegar la pérdida 
	del globo ocular por perforación total.</p>
	
	<p>Los animales que se recuperan quedan con una cicatriz (nube) permanente que 
	afecta la visión y toma relevancia en animales destinados a la reproducción 
	(principalmente los toros).</p>
	
	<p>Todo este proceso puede afectar a uno o los dos ojos. </p>
	
	<p>El diagnostico se realiza principalmente por las manifestaciones clínicas. Los 
	resultados de laboratorio ayudan en la identificación  del agente y la 
	susceptibilidad a los antimicrobianos para el tratamiento</p>
	
	<p><strong>Tratamiento:</strong></p>
	
	<p>Los agentes causantes de esta enfermedad, en general, son susceptibles a casi 
	todos los antibióticos. Los más usados son las oxitetraciclinas LA (link a 
	Maxibiotic) y tilmicosinas (link a Maxityles). Esta última con concentraciones 
	terapéuticas en el globo ocular. (link a Informe técnico II de Maxityl).</p>
	
	<p>También se realizan tratamientos locales con pomadas y spray. Su limitación es 
	por la necesidad de repeticiones en las aplicaciones varias veces al día.</p>
	
	<p><strong>Control y Prevención:</strong></p>
	
	<p>Es fundamental para el control y prevención de la enfermedad disminuir todos los 
	factores predisponentes descriptos anteriormente. Al mismo tiempo se debe 
	implementar la aplicación de vacunas inactivadas piliadas tres semanas antes de 
	la época de presentación de la enfermedad. En primo vacunado aplicar dos dosis 
	con intervalo de 21-28 días. (link a Bioqueratogen Oleo Max)</p>
	
