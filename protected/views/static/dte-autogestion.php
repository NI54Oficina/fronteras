<h3>AHORA PUEDE TRAMITAR TODOS LOS MOVIMIENTOS DE ANIMALES POR INTERNET</h3>
<p>El sistema de autogestión del Servicio Nacional de Sanidad y Calidad Agroalimentaria (Senasa) permite

tramitar los documentos de tránsito electrónico (DT-e) de todas las especies a través del Sistema Integrado

de Gestión de Sanidad Animal (SIGSA). Puede realizar sus movimientos a toda hora, sin necesidad de

dirigirse a las oficinas del Organismo.
</p>

<h4>¿QUÉ NECESITA?</h4>
<p>Para operar sus trámites por internet, necesita mantener actualizada su inscripción en el Registro Nacional

Sanitario de Productores Agropecuarios (Renspa), tener una clave fiscal y disponer de una cuenta corriente

o caja de ahorro (CBU) para efectuar el pago de los aranceles.</p>

<h4>¿CÓMO SE ADHIERE A LA AUTOGESTIÓN?</h4>
<p>Para iniciar la habilitación de este servicio, el primer paso es adherir su CBU a través de la web de la AFIP

con su clave fiscal. Diríjase a “Administrador de Relaciones de Clave Fiscal”, busque “Adherir Servicio”,

después “Senasa”, vaya a “Servicios Interactivos”, seleccione “SIGAD” y cargue e imprima el Formulario de

Adhesión a Pago Directo.</p>

<p>Presente el formulario de adhesión y la constancia del CBU de la caja de ahorro o cuenta corriente que

tenga debidamente certificada por la entidad bancaria, en la oficina local donde opera habitualmente, en el

Centro Regional o en la Sede Central.</p>

<p>Pasadas 72 horas, una vez que constate en la web de la AFIP en el SIGAD o lo notifiquen vía e-mail que su

cuenta fue aprobada, diríjase a la oficina donde emite sus DT-e para solicitar la habilitación en el sistema.</p>

<p>Por último, firme la declaración jurada de adhesión a la Autogestión. A partir de ahora puede operar todos

sus movimientos con el SIGSA, de manera fácil, rápida y desde su casa.</p>

